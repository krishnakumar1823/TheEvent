function openMenuBar(){
    var menubar=document.getElementById("menubar")
    menubar.classList.add("menubarBlock")
}
function menubarClose(){
    var menubar=document.getElementById("menubar")
    menubar.classList.remove("menubarBlock")
}
function closeNav(){
    var menubar=document.getElementById("menubar")
    menubar.classList.remove("menubarBlock")
}